                    <p><span class="senior-associate-dean-content-span-drop-letter">T</span> his year, I crafted my message to focus on a faculty member making a difference as a way to illustrate what’s happening in the College of Media & Communication. With that in mind, I would like to introduce you to John Velez, Ph.D.</p>
                    <p>As an undergraduate, Velez, now an assistant professor in the Department of Journalism & Electronic Media, played a lot of video games. But as a graduate student, he didn’t have time. So instead, he made gaming and gaming culture the focus of his research.</p>
                    <p>“Even as a kid, I was interested in understanding what was happening in a video game,” he said. “I got to graduate school and began melding my interest in gaming with my interest in the effects of video games. It made me enjoy gaming even more because I got to play and apply the things I was learning about in studying them.”</p>
                    <p>In Fall 2014, Velez brought his interest in video game research to Texas Tech and CoMC. He soon found a strong community of gamers in Lubbock who shared his passion but had no outlet at the university. Velez met a couple of CoMC students who had formed what they called Heads Up Display and made YouTube videos about gaming, and he recognized Heads Up Display’s potential as an outlet for students in his classes who were embracing his interest in understanding gaming and gaming culture.</p>
                    <p>They promoted Heads Up Display as a recognized student organization and created their own YouTube channel devoted to gaming and movie reviews.</p>
                    <p>“Heads Up Display grew so fast because this community was already there, and they were just itching to have a place to express themselves,” Velez said. “My role was taking their passion and giving it a structure and kind of guiding them to reach their full potential.”</p>
                    <p>Velez taught them how to organize, manage their time, and even run a meeting. The group soon expanded its influence beyond CoMC by creating HUDcon, a way to express the members’ passion for not just video games but also movies, TV shows, comic books and anime.</p>
                    <p>“In HUDcon, they can come and dress up as their favorite characters and have the opportunity to play different games with different groups around campus,” Velez said. “Groups play certain games, and they set up those games so other people can come and play with them.”</p>
                    <p>HUDcon speakers have included CoMC faculty and alumni, such as the following: industry expert David Swofford, of Star Citizen fame; James Cornell, an artist for the animated TV show "Arthur"; and Steven Holmes of Blizzard, creator of "World of Warcraft." The students’ big question is always, “How did you get there?” They’re often surprised to learn that Swofford graduated in journalism and other industry professionals in electronic media.</p>
                    <p>As for Velez’s research on gaming, Heads Up Display and HUDcon have not provided a direct benefit, but that’s okay with Velez.</p>
                    <p>“It’s been really awesome to discover a side of myself that I didn’t really know was there and to work on other things just as fulfilling as my research,” he said.</p>
