                        <p><span class="all-caps-orange-text">Student athletes dream of a professional career playing the sport they love</span>, and that dream came true for media strategies major Janine Beckie when she was drafted by the Houston Dash in the National Women’s Soccer League in January.</p>
                        <p>“It was kind of unreal,” Beckie said. “I’ve had a dream ever since I was young to play professionally, and so to be drafted and to accomplish something that I’ve been dreaming about since I was a little girl still doesn’t seem real.”</p>
                        <p>Beckie said she started playing soccer when she was 4 years old, living in Denver. The sport gave her a range of opportunities, including a recruitment offer from Texas Tech in 2012.</p>
                        <p>During her time with the Texas Tech women’s soccer team, Beckie was the all-time leading scorer with 57 career goals. And in December 2015, Dallas Soccer News named her Player of the Year.</p>
                        <p>“I got recruited by Texas Tech, and when I came down for a visit, I fell in love with the school and the people and how nice everyone was,” Beckie said. “It’s a great school and a great atmosphere.”</p>
                        <p>In addition to playing soccer with Texas Tech, Beckie made her debut with Canada’s Women’s National Team in 2014. The team qualified for the 2016 Olympics in Rio de Janeiro where they faced teams from nations such as Australia, Zimbabwe and Germany and won the bronze medal.</p>
                        <p>As a student athlete, Beckie has received praise from her professors in the College of Media &amp; Communication who commended her ability to juggle both her career and her education.</p>
                        <p>Andy King, an associate professor of public relations, described Beckie as an excellent student.</p>
                        <p><span class="orange-text">“Janine was as dedicated to her coursework as she was to her sport,” King said. “I think that type of commitment is an important part of what makes a successful student athlete. The requirements are incredibly demanding, and she managed them exceedingly well.”</span></p>
                        <p>Justin Keene, an assistant professor of electronic media and communications, said Beckie exemplified what a student athlete should be through both her work ethic and her commitment.</p>
                        <p><span class="orange-text">“When I think of Janine, I think of the definition of what it means to be a student athlete,” Keene said. “She was so hard working that it was tiring and simultaneously inspiring for me just being around her. Regardless of where her sport took her, she was a student first, and I think that is why she has been so successful on and off the field.”</span></p>
                        <p>While she has taken time off from school in order to keep up with her soccer career, Beckie said she plans to return to Texas Tech in Fall 2016, and she is projected to graduate in May 2017.<span><img class="small-mc-logo" src="images/MCLogo.png" alt="MC Logo"></span></p>
