                    <p>The College of Media &amp; Communication hosted its annual William S. Morris III Distinguished Lecturer series featuring Alison Bryant, CEO and Chief Play Officer of PlayScience, on April 25.</p>
                    <p>Erik Bucy, the Marshall and Sharleen Formby Regents Professor of Strategic Communication in the Department of Advertising, said he was intrigued by Bryant’s experience with PlayScience and her wealth of knowledge and insight into the way children engage with media.</p>
                    <p>“As the chief play officer for PlayScience, Dr. Bryant developed her custom research company from the ground up and built it into a research powerhouse shaping the future of play and learning in the area of children’s media,” Bucy said.</p>
                    <p>Glenn Cummins, associate dean for research in CoMC, said Bryant has been requested by some of the world’s largest corporations to help them understand children and how they use media, and it was an honor to have her speak at Texas Tech.</p>
                    <p>“Dr. Bryant is hands down one of the most dynamic and passionate speakers you’ll ever hear on the subject of children and how the media are a constant part of their lives,” Cummins said. “She’s constantly in demand, both by academics and the industry.”</p>
                    <p>Bucy noted that as a dynamic and electric speaker, Bryant was unanimously selected to be this year’s Morris Lecturer.</p>
                    <p>“Dr. Bryant is highly qualified as both a researcher and industry practitioner,” Bucy said. “Before starting PlayScience, she was on the faculty at Indiana University and then worked for Nickelodeon. She can speak to both an academic and industry audience.”</p>
                    <p>Bryant said she hopes that her lecture inspired audience members to think more broadly about the opportunities for researching how children interact with media to make even more of an impact.</p>
                    <p>“It is such a pleasure to engage with an audience that not only is interested in your topic, but also is so passionate about it and doing their own amazing work in the field,” Bryant said. “I was pleased to see over the course of my time at TTU that the faculty and students are doing such top-notch, impactful work in the space—it gave me great ideas and made me want to do even more!”</p>
                    <p>Cummins described the Morris Lecture series as the perfect event to bring together academic researchers and professionals in the media.</p>
