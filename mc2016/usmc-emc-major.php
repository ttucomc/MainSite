                <div class="large-6 columns">
                    <p>Texas Tech University’s recognition as one of the top schools in Military Advanced Education & Transition’s 2016 Guide to Colleges and Universities and its reputation for veteran relations were two things that attracted electronic media and communications major Simon Parmley to the College of Media & Communication.</p>
                    <p>Originally from Seattle, Parmley described himself as the classic, textbook example of a nontraditional student.</p>
                    <p>“I knew I wanted to explore the world after high school, and I joke that that is when the commercial came on for the Marine Corps, and I decided to join,” Parmley said. “I enlisted as a helicopter mechanic in 2006, and my first duty station was in Okinawa, Japan.”</p>
                    <p>After two years in Japan, Parmley said he realized that there was not a lot of room for creative interpretation when working on helicopters, and he began to look for a job that would let him utilize his creativity.</p>
                    <p>“While I was there, I became a marksmanship instructor,” Parmley said. “I was building helicopter engines, teaching marksmanship, and then I found an opportunity to volunteer for embassy guard duty.”</p>
                    <p>Parmley said he began working as an embassy guard in 2009, which allowed him to travel to spend a year in Prague, a year in Kampala, Uganda, and a year in London.</p>
                    <p>“The best example of an embassy guard is in the Jason Bourne movies where he goes into the embassy, sets off the alarms, and Marines just run out of the woodwork and he beats them all up,” Parmley said. “That is the job I did, but I didn’t get beaten up by Matt Damon. In reality, it was much closer to working as a corporate security guard.”</p>
                    <p>While it wasn’t his dream job, working as an embassy guard allowed him to visit several other countries, and he was able to express his creativity through photography.</p>
                    <p>“Being in the places I was compensated for the very mundane day job,” Parmley said. “I had a blast, and I do not in any way regret my time in the Marine Corps. It was an incredible experience.”</p>
                    <p>Parmley left the Marines in 2012 and moved back to Seattle. After a few years of working odd jobs, he said he decided to commit to getting his degree, and he moved to Lubbock and began attending Texas Tech in Fall 2015.</p>
                </div>

                <div class="large-6 columns">
                    <p>“When I decided to choose a path, I chose to come to Texas Tech,” Parmley said. “If you look at schools that are incredibly veteran-friendly and that have a really good digital media program, the Venn diagram overlaps at Tech.”</p>
                    <p>Parmley said his favorite class during his first semester at Texas Tech was Visual Storytelling because it was focused on crafting great images, regardless of whether it’s in film, photography or graphic design. It’s focused on how to tell a good story.</p>
                    <p>Jerod Foster, Ph.D., an assistant professor of practice in journalism & electronic media and the instructor for Visual Storytelling, described Parmley as a great leader who was very vocal in the class.</p>
                    <p>“He’s really good at applying the content we go over in class to the practical component,” Foster said. “His group’s final project was by far the one that shined the most in the class. It had a great conceptual story to it. It had great visuals, and the group was able to tie Simon’s experience with the military into the video. Simon especially stood out for his leadership and the way that he led the production of the video.”</p>
                    <p>Foster said he thinks the fact that Parmley has life experiences most traditional students don’t have works to his advantage.</p>
                    <p>“Simon had a chance to really consider what he wanted to do with his life after the military, and this is it,” Foster said. “He wants to be a visual storyteller. He doesn’t know exactly where he wants to land, but he knows he is taking advantage of all of the theoretical and practical opportunities to soak in knowledge while he is here.”</p>
                    <p>Parmley said he chose to pursue photography because it is a creative outlet he has turned to throughout his life, and everywhere he has traveled, he has found something that was worth documenting.</p>
                    <p>“Photography allows me to capture events in the way that I see them,” Parmley said. “It’s a creative route where only you can create the pictures you see in your head.”<span><img class="small-mc-logo" src="images/MCLogo.png" alt="MC Logo"></span></p>
                    <p>(Rachel Blevins is a junior journalism major from Mineral Wells, Texas.)</p>
