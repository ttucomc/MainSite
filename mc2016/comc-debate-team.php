                    <div class="large-4 columns">
                        <p>The Texas Tech Debate Team, housed in the College of Media &amp; Communication, started the 2015-16 season by breaking university records with the help of a new director of forensics.</p>
                        <p>Adam Testerman (BA communication studies, 2012) returned to Texas Tech to work as the director of forensics in July 2015 after earning a master’s in communication studies from Portland State University in May.</p>
                        <p>“I have been debating for a long time,” Testerman said. “I started my freshman year of high school, and I’ve been involved in some capacity ever since.”</p>
                        <p>Originally from Springfield, Mo., Testerman was recruited to the debate team at Texas Tech as an undergraduate student. He helped the team win its second national championship and was the first Texas Tech student to be named the top individual debater in the country at the National Parliamentary Tournament of Excellence in 2010.</p>
                        <p>Texas Tech’s debate team competes in parliamentary style, which Testerman said he enjoys because it presents the contestants with a unique challenge and requires them to think on their feet.</p>
                    </div>

                    <div class="large-4 columns">
                        <p>“It’s a really unique style because the topic changes every single debate round,” Testerman said. “We get a topic and then have 20 minutes to prepare for it, so it’s a very intense way of doing it. It really brings in a lot of the elements that I think make debate great.”</p>
                        <p>Currently, the team has 13 members, and Testerman said that while anyone is welcome to join, being a member requires commitment, and the team’s schedule can be very demanding.</p>
                        <p>“Anyone who wants to figure out if it’s for them is encouraged to come hang out with us,” Testerman said. “But we have fairly high standards for what it takes to remain a member of the team. There are a lot of expectations for tournament travel, for attending practices, and for researching material that needs to be produced every single week.”</p>
                        <p>When it comes to his vision for the team, Testerman said it relies on two main things: hard work and utilizing university resources. He said he encourages students to take the information they learn in class and to find ways to apply it in a debate context.</p>
                    </div>

                    <div class="large-4 columns">
                        <p>"We expect to be the team that is ready for any kind of argument, and we expect to be the best researched team in the nation,” Testerman said. “Other teams might beat us in an individual round, but no one gets to outwork us.”</p>
                        <p>Testerman said the debate team is one of the oldest programs on campus, and since the Department of Communication Studies merged with CoMC, it has flourished with support from the administrative level.</p>
                        <p>The teams attended three tournaments during Fall 2015, and Testerman said he was very pleased with the results. At the first tournament, two teams made it to the top 16, and Texas Tech finished in fourth place, which Testerman noted is the best performance the team has had in at least 10 years.</p>
                        <p>At the second tournament, one team reached the final round, and Texas Tech won the award for top speaker after three of the team’s speakers reached the top 15. The third tournament of the season took the team to Lewis &amp; Clark College, where Testerman was previously a debate coach.</p>
                        <p>“We did really well at the Lewis &amp; Clark College debate tournament,” Testerman said. “We had two speakers in the top 15, and both of our teams cleared to elimination rounds. One of our teams made it to quarterfinals, securing a finish in the top eight. And our other team made it all the way to semi-finals, securing a finish in the top three teams.”
                        </p>
