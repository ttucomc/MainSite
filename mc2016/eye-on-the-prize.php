                    <div class="large-6 columns">
                        <p>Journalist Kaitlyn Kravik (BA journalism, 2015) joined ESPN as a member of the network’s content associate program in June in Bristol, Conn., where she supports the production of live studio shows  and assists in location productions.</p>
                        <p>Kravik’s career has led her to this point, and it all started in January 2013 when she began reporting for “Double T Insider,” the official sportscast of Texas Tech Athletics that is produced by CoMC students.</p>
                        <p>Kravik said she was also one of the members responsible for pitching the show to Fox Sports Southwest, where it now airs weekly. She said working in the world of sports media was something she always wanted to do.</p>
                        <p>“I grew up a Rangers fan, above anything else,” Kravik said. “Now I’m getting to work in sports, which is what I’ve wanted to do for so long now, and it’s all coming full circle.”</p>
                        <p>Kravik wanted to pursue a job with the Texas Rangers, and she began networking with Chris DeRuyscher, the senior director of In Park Entertainment for the Rangers, in November 2014.</p>
                        <p>While there weren’t any spots available at the time, DeRuyscher reached out to Kravik in March 2015 and asked if she was interested in bringing back the position of an in-game host for the Rangers games.</p>
                        <p>Kravik noted that it had been five years since there was an in-game host at the Ballpark in Arlington, which gave her the opportunity to bring a new meaning to the title. She began by working the exhibition games on the weekends during the spring semester before she graduated.</p>
                        <p>“Everything was live, which is very nerve-racking, especially when you can hear yourself and there is a bit of a delay,” Kravik said. “It was a lot to get used to, but it was also a lot of fun, and I enjoyed it so much.”</p>
                        <p>In addition to working with the Rangers, Kravik had the opportunity to assist in ESPN’s production of the 2015 U.S. Open tennis tournament in New York. She said it started when she was networking with a producer at the MLB network, and everything seemed to fall into place.</p>
                    </div>

                    <div class="large-6 columns">
                        <p>When the Rangers’ season came to a close in Fall 2015, Kravik started working as a video producer for the Dallas Mavericks. She said her initial job was to operate the 4K camera, which is used to replay footage of disputed plays.</p>
                        <p>“My first role with the Mavericks was to work as a 4K camera operator during the home games,” Kravik said. “I followed the entire game, and anything that is questionable is what my footage was for – to be looked at and reviewed by Mavs owner Mark Cuban. No pressure!”</p>
                        <p>Kravik said she also worked as a blogger and an editor for the Mavericks, producing videos that featured highlights from games and meet-and-greets with fans. In addition to regular highlights, Kravik also featured the highpoints of each game in a segment called “Mavs Minute,” which she hosted and produced.</p>
                        <p>“The ‘Mavs Minute’ was really fun, because I have creative control,” Kravik said. “It’s a small recap of a game mixed with current promotions for home games, and I produced all of it myself – I shot the standup myself and then I would go back and edit it.”</p>
                        <p>Kravik said she would advise current CoMC students to network as much as possible while they are in school, especially if they want to go into a profession in broadcast journalism or sports media. She encouraged them to be both patient and persistent and to reach out to the professionals in their field of interest.</p>
                        <p>“Talk to everyone you possibly can, and don’t be afraid to ask for advice, especially when it comes from someone who is in the place you want to be,” Kravik said. “That’s what I’ve done. I’ve really put myself out there and said ‘This is what I’ve done. This is who I am.’ I’ve also asked for advice moving forward.”<span><img class="small-mc-logo" src="images/MCLogo.png" alt="MC Logo"></span></p>
