                    <p>
                        Working at KJCT-TV and KKCO-TV, Smith (BA electronic media &amp;
                        communication, 2015) said she covers weather over the weekend and
                        works as a reporter during the week.
                    </p>
                    <p>
                        “I started January 4 and after about four days of training, I did
                        the weather for the Friday noon show for KKCO,” Smith said. “Then I
                        did the weekend shows all by myself, and I had to cover for the chief
                        meteorologist. That was exciting. It’s great that they already trust me
                        to do that.”
                    </p>
                    <p>
                        Originally from Mansfield, Texas, Smith said Lubbock was the
                        farthest west she had been before she moved to Grand Junction, and
                        her new job marked the first time she had ever been to Colorado. She
                        said she found the station while she was searching for weather-related
                        broadcast jobs online.
                    </p>
                    <p>
                        “During the summer, I was applying for any weather job
                        I saw on the Internet,” Smith said. “I saw that there was a
                        morning weather position at KKCO, but when I applied I
                        never heard back from them. I assumed they didn’t care, and
                        I moved on with my life.”
                    </p>
                    <p>
                        However, Smith said she received an email from the
                        station’s news director in November, asking if she was
                        interested in a weekend weather anchor position instead, and
                        she said she was happy to accept.
                    </p>
                    <p>
                        Smith began her time at Texas Tech as a geophysics major.
                        During her junior year, she changed her major to electronic
                        media &amp; communication with a minor in atmospheric science
                        to pursue a budding interest in TV meteorology.
                    </p>
                    <p>
                        In addition to interning with KCBD-TV in Lubbock for
                        two-and-a-half years, Smith said she gained experience as a
                        student by working with CoMC’s student-produced newscast,
                        "MCTV Weekday Update."
                    </p>
                    <p>
                        CoMC’s media production manager, Josh Robinson, said
                        Smith was the first student to anchor a weather segment on
                        MCTV after the newscast launched in 2008. He said Smith
                        started out by doing live-shots outside the building, and then
                        when the studio was redesigned in 2014, she became the first
                        student to anchor weather in studio for MCTV.
                    </p>
                    <p>
                        “I personally redesigned the TV studio in 2014 to include
                        a weather wall due to Carly’s involvement with the newscast.
                        Up until then, we only had the main news set and the green
                        screen behind it,” Robinson said. “When Carly continued to
                        show interest in doing weather, I designed the side set, and
                        bought a couple additional video monitors and some extra
                        lighting to make it all happen.”
                    </p>
                    <p>
                        Robinson described Smith as a unique student who set
                        a high standard in the college, and he said he is glad she
                        branched out and decided to try anchoring weather with
                        MCTV.
                    </p>
                    <p>
                        “Carly is a smart, energetic and motivated individual who
                        has the ability to do great things,” Robinson said. “I’m really
                        glad the management at KKCO recognized her abilities, and I
                        think Grand Junction is just the first stop for her as she works
                        her way up the ladder of the broadcast industry.”
                    </p>
                    <p>
                        Smith said one of her favorite things about Texas Tech is
                        that the professors are “top notch” and were always there to
                        answer her questions.
                    </p>
                    <p>
                        “Even in December after I graduated I was asking Josh
                        about finding a job and if he knew anything about KKCO,”
                        Smith said. “Students should take the time to get to know
                        their professors because they want you to succeed. They also
                        have connections that can help you find a job.”
                    </p>
                </div>
                <div class="small-12 large-6 columns">
                    <p>
                        Smith said her advice for current students is to get as
                        much experience as possible before they graduate and to stay
                        persistent when searching for a job after graduation.
                    </p>
                    <p>
                        “The biggest advice I can give is to get experience, whether
                        it’s through your college program like MCTV or through
                        interning at a station,” Smith said. “Don’t be afraid to intern
                        at more than one place because you’ll get more connections
                        that way, and connections in the media industry are really
                        important. Also, don’t give up on your job search. It’s going to
