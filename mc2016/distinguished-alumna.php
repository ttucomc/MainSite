        <section class="distinguished-alumna-content">
            <div class="row">
                <div class="large-6 columns">
                    <p>Laura Nieto, senior manager of community affairs and grassroots at Southwest Airlines, was one of three graduates recognized by the Texas Tech Alumni Association as a 2016 Distinguished Alumni of Texas Tech University on Feb. 26.</p>
                    <p>Nieto (BA public relations, 1995) said it was surreal to receive the news of her award, via a phone call from Bill Dean, Ed.D., who was her professor for the Introduction to Mass Communications course.</p>
                    <p>“I was blown away when I received the news,” Nieto said. “I agree with the thought ‘To whom much is given, much is expected.’ It is an honor that I don’t take lightly and am humbled by the recognition. I am a proud Red Raider and carry the Texas Tech banner far and wide!”</p>
                    <p>Dean, an associate professor in the College of Media & Communication, said he remembers Nieto as a driven student who used her work ethic in college to excel in the professional world.</p>
                    <p>“I remember her when she was in my class. She was hard working and very focused,” Dean said. “I think she carried this approach into her professional life and it has paid off. She was very worthy of this recognition.”</p>
                    <p>Nieto launched her career during her last semester at Texas Tech when she interned with an ad agency in her hometown of San Antonio. After just a couple of months, she was offered a job as an account executive.</p>
                    <p>“At the time, it was one of the most well-known Hispanic ad agencies in the country and was even recognized as the Best of the Southwest by Ad Age magazine,” Nieto said. “After only a couple of months, I was hired on as an account executive doing promotions for some blue chip clients, including Coca-Cola and Western Union.”</p>
                    <p>Nieto said she left the agency after one of her clients, who left Coca-Cola to develop the multicultural marketing department at Southwest Airlines, recruited her to join the team in 1999. Nieto was promoted to manager of the advertising, promotions and community affairs department at Southwest in 2003 and became the senior manager of community affairs and grassroots in 2007.</p>
                    <p>Jerry Hudson, Ph.D., dean emeritus of CoMC, said he is not surprised by Nieto’s success with Southwest given the fact that she was a hardworking student who took advantage of opportunities in college.</p>
                </div>

                <div class="large-6 columns">
                    <p>“I remember Laura as a very serious student who took advantage of networking opportunities, being involved in many of our activities, and focusing on learning relationships between theory and practical experience,” Hudson said.</p>
                    <p>Nieto said she loves her department at Southwest because she is passionate about community outreach to diverse audiences, and her job gives her a way to develop strategic relationships with key constituencies and national leaders with various segment audiences.</p>
                    <p>Nieto also leads the company’s employee engagement and grassroots advocacy programs where she said she works to keep Southwest employees informed and engaged in meaningful constituent relationships with their community leaders and elected officials.</p>
                    <p>Linda Rutherford (BA journalism, 1988), vice president and chief communications officer for Southwest Airlines, said Nieto truly understands the fact that the airline’s mission is to connect with the people in the communities they serve.</p>
                    <p>“Laura’s high emotional intelligence is an asset to us here at Southwest Airlines,” Rutherford said. “She instinctively understands the importance of relationships and helps guide her teams in making beneficial, long-lasting and strategic connections with community influencers and organizations.”</p>
                    <p>When it comes to her proudest accomplishments, in addition to being named as one of Texas Tech’s Distinguished Alumni, Nieto noted being selected as a member of Southwest’s company-wide culture committee, which is a group of select employees who seek to create and enhance the Southwest spirit and culture that makes the airline unique.</p>
                    <p>“I am most passionate about identifying opportunities for my family and my community to influence positive change and create unforgettable experiences,” Nieto said. “Whether I’m traveling to places near and far with my family, teaching Girls-In-Action, a 4th grade missions class at church, or sponsoring students and young professionals on trips … I’ve got the best of both worlds and have made a career in building relationships and making a difference in communities all across the country.”<span><img class="small-mc-logo" src="images/MCLogo.png" alt="MC Logo"></span></p>
