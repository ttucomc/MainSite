                <div class="row">
                    <div class="large-6 columns">
                        <p>The Texas Tech University College of Media &amp; Communication launched the Communication Training Center in July as part of the university’s campus Quality Enhancement Plan.</p>
                        <p>David Perlmutter, dean of the college, said that as one of the pillars of the campus QEP, the CTC will help increase the quality of various aspects of communication throughout the university and beyond.</p>
                        <p>“We hope to make a tremendous impact across campus for the improvement of visual and oral communication in teaching,” Perlmutter said, “as well as beyond in scholarly publishing, engaging nonacademic audiences, and generally in science and health communication.”</p>
                        <p>Lawrence Schovanec, Texas Tech president, agreed and said that CoMC’s role in creating the CTC will help with the new initiative to improve students’ communication skills and multicultural awareness.</p>
                        <p>“The CTC will play an essential role in helping Texas Tech meet the objectives of our recently adopted Quality Enhancement Plan,” Schovanec said. “Through the CTC, and supported by the expertise in the College of Media &amp; Communication, students and faculty will be better trained and informed about this new initiative.”</p>
                        <p>Todd Chambers, associate dean for undergraduate affairs, said the process for creating a new QEP started about two-and-a-half years ago as part of the university’s accreditation process with the Southern Association of Colleges and Schools’ Commission on Colleges.</p>
                        <p>“We were charged with developing a new quality enhancement plan that we felt was beneficial for every student at Texas Tech,” Chambers said. “A campus committee came up with the theme ‘Communicating in a Global Society.’ Part of that plan was to create a center for global communication that would help coordinate global communication issues that are happening already across campus.”</p>
                        <p>Gary Smith, budget director for the College of Architecture and head of the committee that created the CTC, said that with the theme of “Communicating in a Global Society” in mind, they wanted students to be able to use new software and social media in appropriate ways to communicate globally.</p>
                        <p>“The Communication Training Center was created to help faculty learn ways to incorporate these tools into coursework and to help students learn how to use the tools effectively,” Smith said.</p>
                    </div>

                    <div class="large-6 columns">
                        <p>Genevieve Durham DeCesaro, associate vice provost for academic affairs, said she believes the CTC will work to enhance the value of communication throughout various academic disciplines.</p>
                        <p>“The Communication Training Center is poised to augment the university’s focus on the critical nature of communication and how communication is valued, commonly and distinctly, across academic disciplines,” Durham DeCesaro said.</p>
                        <p>Chambers said the CTC would help complement the university writing center by preparing Texas Tech students to compete in a global marketplace through strengthening their ability to communicate verbally and visually.</p>
                        <p>“Ultimately, the CTC is going to affect undergraduate students,” Chambers said. “We want to start by giving teachers, instructors and faculty members here at Texas Tech the resources to say ‘What does communicating verbally and visually in a global marketplace look like?’”</p>
                        <p>The creation of the CTC included hiring a new director, Luke LeFebvre, who has experience as the director of three award-winning communication centers at the College of Southern Nevada. He said he hopes to see the CTC become a hive of activity on Texas Tech’s campus.</p>
                        <p>“I foresee workshops occurring at the center as well as CTC staff venturing out to work with scholars across the institutional landscape at Texas Tech University,” LeFebvre said. “Our purpose will be to help scholars bring their ideas to life through content development, competent and confident oral communication, and powerful visual presentations.”<span><img class="small-mc-logo" src="images/MCLogo.png" alt="MC Logo"></span></p>
