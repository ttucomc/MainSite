                    <h4 style="color: #4d7691;" class="font-bromello"><span class="large-h4">Texas Tech</span> sophomore Alana Krafsur said “goodbye” to a life that revolved around ballet and “hello” to the pursuit of a career in public relations when she joined the College of Media &amp; Communication in Fall 2015.</h4>
                </div>
            </div>

            <div class="row">
                <div class="large-6 columns">
                    <p>Originally from El Paso, Krafsur said the 15 years she spent in ballet led to an abundance of opportunities, including trips across the country to train with renowned teachers. In the summer of 2013, Krafsur was one of 15 students chosen to attend the Bolshoi Ballet Academy in Moscow, Russia, for six weeks on full scholarship.</p>
                    <p>“It was funded through the National Security Language Initiative for Youth and sponsored by the U.S. State Department,” Krafsur said. “It was really concentrated in ballet, but also really concentrated in the Russian language, so we had Russian classes every day for four hours a day.”</p>
                    <p>Although Krafsur was one of three American students in her group invited to stay at the academy through the 2013-14 academic year, she said she declined the offer in order to spend her senior year of high school with her family and friends.</p>
                    <p>Krafsur then went on to study dance at the University of Arizona, which has the top-ranked public school dance program in the country. However, she said that while she enjoyed the program, she began to question whether she wanted to dance for the rest of her life.</p>
                    <p>“I gave it a whole year, but I started having some doubts,” Krafsur said. “I just couldn’t see myself dancing professionally anymore. So I made a really rushed decision to end ballet altogether and to go to Texas Tech for PR.”</p>
                    <p>Krafsur said that while giving up a career she has known her whole life is the biggest risk she has ever taken, she has been happy with the results, and she is thankful for the PR lessons she was able to learn during her time as a ballerina.</p>
                    <p>“At the end of the day I realized that my experience really utilized PR,” Krafsur said of her time in Moscow. “In my mind we were student ambassadors, and we were creating positive relations between two countries – Russia and the U.S.”</p>
                    <p>During her first semester at Texas Tech, Krafsur said one of her favorite classes was Visual Storytelling, where she had the opportunity to work with a group to create a video that highlighted acts of kindness around campus.</p>
                </div>

                <div class="large-6 columns">
                    <p>The instructor of the course, Jerod Foster, Ph.D., said that with the class he tries to bridge the conceptual with the practical, while teaching his students about visual literacy.</p>
                    <p>“The class not only defines story, it also breaks down, or deconstructs, how great visual storytellers create imagery of all kinds to compel and engage an audience,” Foster said. “It’s akin to a behind-the-scenes, how-stuff-works approach to the mechanisms that go into grabbing a viewer and sucking them into something we are all attracted to: a story.”</p>
                    <p>Todd Chambers, Ph.D., associate dean for undergraduate affairs and Krafsur’s instructor for the course Success in Media &amp; Communication, said it wasn’t until she submitted her “e-portfolio” that he learned about her former career.</p>
                    <p>“Once I read through her background, I was blown away,” Chambers said. “She definitely has had unique experiences and represents the type of outstanding students we have here in the College of Media &amp; Communication.”</p>
                    <p>In addition to her sister attending Texas Tech, Krafsur said she has several family members who are Red Raiders, and the university was her first choice once she decided to retire from ballet.</p>
                    <p>“When I decided I didn’t want to pursue dance anymore, the first and only option I went after was Texas Tech,” Krafsur said. “Ever since I arrived, I have felt right at home. Everyone has been friendly, and I have loved my classes.”</p>
                    <p>When it comes to other students who might be considering major changes in their career paths, Krafsur said she would encourage them to pursue the thing that makes them happy.</p>
                    <p>“If you give it a full year, and you’re still having a lot of doubts, and you’re finding joy in thinking about another major or career, then take a risk, take a leap of faith, and take another route,” Krafsur said. “In my experience, all of it paid off and I’m happier than ever, so I would want students to follow what truly makes them happy.”<span><img class="small-mc-logo" src="images/MCLogo.png" alt="MC Logo"></span></p>
