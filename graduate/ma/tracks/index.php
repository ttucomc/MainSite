    </p>
    <h4>Concerning Final Project</h4>
    <p>All Sports Track graduate students will complete a “final comprehensive evaluation” in the form of a capstone final project that focuses on sports media. The course, which will be taken in the student’s final semester:</p>
    <ul>
      <li>will use the current COMC 6050 designation.</li>
      <li>will be listed under a graduate faculty member.</li>
      <li>will require students to complete a research project focusing on sport media.</li>
      <li>will require students to orally present assignment to graduate faculty.</li>
    </ul>
