  <h2>All tracks in the Master of Arts in Mass Communication Program require 30 hours.</h2>
  <ul>
    <li>The <a href="#sports">sports media track</a> assists students in obtaining jobs in the fast-growing sports entertainment industry.</li>
    <li>The <a href="#thesis">thesis track</a> prepares students for in-depth studies in theory and research as well provides the foundation for the pursuit of a Ph.D. degree.</li>
    <li>The <a href="#general">general professional track</a> allows students to develop additional professional skills across a variety of media fields.</li>
    <li>All non-thesis students will complete a “final comprehensive evaluation” in the form of a capstone final project (MCOM 6050 - Final Project).</li>
  </ul>
