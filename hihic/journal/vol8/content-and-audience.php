<section>
  <h2>Descripción de los magacines de máxima audiencia</h2>
  <p>A continuación, se describirán las características formales y los contenidos de los programas magacines de máxima audiencia en Argentina y España en 2013.</p>

  <h3>Argentina</h3>

  <p>Los magacines de la radio argentina que se analizan en este trabajo se emiten en AM/OM, que sigue siendo la banda de emisión de la radio generalista, y cuyo eje es la información, la opinión, el entretenimiento y el humor. Según los estudios de IBOPE, en la franja horaria de 6 a.m. a 1 p.m. la audiencia acumulada es de 8.222.836 oyentes. La programación de mañana se reparte en dos programas diferentes, con presentadores y equipos propios, y con registros y contenidos que se adecúan a las horas del día: la <em>primera mañana</em>, de 6 a.m. a 9 a.m. es más informativa; y la <em>segunda mañana</em>, de 9 a.m. a 1 p.m. persigue el entretenimiento. Teniendo en cuenta esta peculiaridad, se describen aquí los ocho programas más escuchados en 2013 en ese tramo horario: <em>Cada mañana y Lanata sin filtro</em> (Radio Mitre), <em>Hola Chiche y El oro y el moro</em> (Radio 10), <em>Magdalena tempranísimo y La mañana</em> (Continental), <em>Empezando el día y Ciudad Goti K</em> (La Red).</p>
  <p><strong><em>Cada mañana</em></strong> es uno de los programas más escuchados del <em>prime time</em> de la radio argentina desde sus primeras emisiones en el año 2000. Durante 2013 y 2014 ha ocupado la primera mañana de Radio Mitre y se ha convertido en el programa con mayor índice de audiencia.</p>

