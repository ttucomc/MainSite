<section>
  <h2 id="note-line-1">Resumen<sup><a href="#note-1">[1]</a></sup></h2>
  <p>Este trabajo compara el comportamiento de las audiencias de los programas líderes del <em>prime time</em> de la radio generalista en Argentina y España con el fin de identificar las similitudes y diferencias entre dos mercados de referencia en el mundo hispano. Se describen primero las peculiaridades formales y narrativas de cada magacín, para luego caracterizar la audiencia de cada uno de los programas y obtener conclusiones de dicha observación. El análisis realizado confirma que el magacín de actualidad es un tipo de programa muy homogéneo en cuanto a contenidos y recursos de producción en todas las emisoras estudiadas. Con algunas diferencias en la identificación del <em>prime time</em>––de 8 a.m. a 9 a.m. en España y de 10 a.m. 11 a.m. en Argentina––el magacín mantiene su vigencia entre el target adulto sin atraer a la audiencia joven, que no elige la radio para consumir información. Asimismo, a la oferta de contenidos basada en la información y la opinión incorpora el entretenimiento temático, la conversación con colaboradores y la interacción con la audiencia para conseguir y mantener oyentes.</p>
  <p><strong>Palabras clave:</strong> programas de radio, audiencias de radio, radio <em>prime time</em></p>

  <h2>Abstract<sup><a href="#note-1">[1]</a></sup></h2>
  <p>This paper compares the behavior of leading prime-time radio programs in Argentina and Spain in order to identify the similarities and differences between two important radio markets in the Hispanic world. First, it describes the formal and narrative characteristics of each radio magazine program, and then it describes the audiences for the programs in order to draw conclusions from the observations. The analysis confirms that the radio magazine is a homogeneous program genre in terms of content and production resources in all of the stations studied. With some differences in the identification of prime time––8 a.m. to 9 a.m. in Spain and 10 a.m. to 11 a.m. in Argentina––the radio magazine remains popular among adult listeners without attracting young audiences, who seldom use radio to consume information. Furthermore, the information- and opinion-based content incorporates entertainment elements, conversations among hosts, and interactions with the audience in order to keep listeners tuned in.</p>
  <p><strong>Keywords:</strong> radio programs, radio audience, radio prime time</p>
