  <h2>Conclusiones</h2>
  <p>Los programas magacines del <em>prime time</em> que han sido hasta ahora la columna vertebral de la programación generalista en los mercados de la radio argentina y española experimentan cambios caracterizados por el desplazamiento del horario, el predominio del contenido informativo y la presencia de las voces autorizadas del periodismo, en detrimento del contenido de entretenimiento y de los presentadores estrella. </p>
  <p>El magacín como programa contenedor que pivota sobre la actualidad del día y con una importante producción periodística sigue teniendo vigencia y las mejores audiencias en las mañanas de la radio de Argentina y España. En ambos países sigue siendo un tipo de programa con variedad  de temas y de voces, administrado por un conductor protagónico que alterna constantemente una oferta muy variada para aportar ritmo, en un contexto en que el oyente-usuario elige, programa y participa. No obstante, se aprecian algunos cambios en el comportamiento de la audiencia, que adelanta la escucha y se estanca a pesar de los intentos de hacer una radio más desenfadada en algunos tramos horarios. </p>
  <p>Una de las notas distintivas de los magacines del <em>prime time</em> de Argentina y España es la construcción de una identidad fuerte y coherente de los programas. Se administran contenidos heterogéneos y voces variadas que entran y salen del aire de modo intermitente, buscando continuidad narrativa y coherencia en la diversidad. Se prefieren como presentadores a profesionales del periodismo de reconocido prestigio que generan opinión y aglutinan grandes audiencias, en detrimento de voces que sólo hacen de enlace entre las partes del programa y los contenidos comerciales. </p>
  <p>El ritmo programático de las mañanas de la radio generalista tiene dos momentos claramente definidos: la primera mañana, más informativa; y la segunda mañana, más lúdica, temática y participativa. En Argentina, se programan dos espacios diferentes, pero con una transición que les da continuidad (“el pase”). En España, las emisoras mantienen un único programa y marca informativa, de seis horas de duración, pero al menos en dos casos, con presentadores diferentes a partir de las 10 a.m. para marcar ese cambio de ritmo y contenidos. Teniendo en cuenta esta peculiaridad, se hace necesario estudiar las audiencias de forma discriminada, atendiendo a estos dos momentos claramente diferentes en la escucha.</p>
  <p>La distribución de los contenidos es bastante homogénea en todas las emisoras; se juega con los mismos ingredientes en las mismas horas del día. Los programas arrancan con una alta presencia de la información y la opinión en las tres primeras horas, y se abren hacia el entretenimiento temático, la conversación y la interacción con la audiencia a partir de las 9 a.m. y las 10 a.m. La oferta de contenidos es, por tanto, muy similar y las diferencias tienen que ver más con el estilo de la presentación y los recursos de producción de cada programa. Además, los magacines de las emisoras argentinas se caracterizan porque suman el humor desde la primera mañana como un ingrediente más del relato de la información del día.</p>
  <p>Tanto en España como en Argentina, la mañana de la radio ha ido ganando en entretenimiento a través de los años. Los magacines se centran en la información de actualidad sin perder de vista una de sus funciones principales: entretener. Los presentadores, sobre todo, y los colaboradores del programa resultan piezas fundamentales de una narración que integra permanentemente información y entretenimiento, humor y distensión.</p>
  <p>En la actualidad, el <em>prime time</em>, que durante décadas estaba entre las 11 a.m. y el mediodía, se adelanta en la radio generalista. En España, es más informativo y se encuentra en la primera mañana, a las 8; en Argentina se mantiene en la segunda mañana pero se adelanta a las 10.</p>
  <p>En ambos mercados, y atendiendo a la audiencia acumulada, se escucha más la radio temática. La tendencia cambia si se analizan los tramos horarios de mañana durante la emisión de los magacines, en donde las audiencias son similares o bien lideran levemente la radio generalista. Esto ha llevado a programar magacines despertadores o <em>morning shows</em> en las principales cadenas especializadas en el caso de España, y a adoptar formas mixtas de programar durante las mañanas de la radio temática en Argentina, que se acercó en los últimos años a la propuesta generalista del <em>prime time</em> (conductores-estrella, magacines con información de actualidad y diversidad de temas y de voces).</p>
  <p>Finalmente, la audiencia de los magacines es mayoritariamente adulta, con más de un 55% de oyentes mayores de 45 años, de clase media y alta en ambos mercados. Se confirma la tendencia de que la audiencia joven no elige la radio tradicional para consumir información y prefiere las nuevas plataformas que incrementan su cuota de mercado, sobre todo en dichas franjas de edad. Por todo lo dicho, el estudio confirma que los programas magacines siguen funcionado como fórmula radiofónica en la radio generalista, pero su audiencia se estanca. Por ello, deberían apostar por investigar e innovar en la oferta informativa de la primera mañana, de buscar otras formas de comunicarse con la audiencia joven y de comercializar mejor su producto. Así conseguirían adaptarse mejor a los cambios de consumo radiofónico de la audiencia.</p>
