  <h3>España</h3>

  <p>Los magacines de la radio española que se analizan en este trabajo se emiten simultáneamente en OM y FM, siendo ésta última la banda de emisión que acumula más audiencia. En la franja horaria de 6 a.m. a 1 p.m., la audiencia acumulada es de 9.455.394 oyentes. El público objetivo predominante de estos programas es adulto, mayor de 45 años. Desde el punto de vista de la programación, la mañana forma parte de un bloque continuo de emisión, con algunas diferencias entre emisoras. No obstante, desde hace tres temporadas, algunos de estos magacines identifican dos momentos diferentes y, aunque mantienen la marca del programa, cambian de presentadores, de equipos y de estilo a partir de las 10 a.m. Es decir, distinguen también la primera mañana, más informativa; y la segunda mañana, más lúdica, temática y participativa. Teniendo en cuenta estas peculiaridades, se describen aquí los programas más escuchados en 2013 en el tramo horario objeto de estudio: <em>Hoy por hoy</em> (Cadena SER), <em>Herrera en la onda</em> (Onda Cero), <em>La mañana</em> (Cadena COPE) y <em>Las mañanas de RNE</em> (Radio Nacional de España).</p>
