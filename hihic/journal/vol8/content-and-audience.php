  <h2>Introducción</h2>
  <p>A lo largo de las últimas cuatro décadas, el programa magacín se ha constituido como la columna vertebral de la oferta de la radio generalista, alcanzando las mayores cuotas de audiencia y convirtiéndose en el programa de más larga duración en el esquema de franjas horarias de la programación. El magacín es un programa maratón que ocupa entre tres y seis horas de la emisión por término medio de minutado. Se le conoce con frecuencia como un programa de contenidos variados pero de estructura reiterativa y coherente, convenientemente construida por un presentador con grandes capacidades comunicativas.</p>
  <p>El magacín se ha convertido en el <em>programa-tractor</em> para la captura, suma y arrastre de audiencias millonarias en bloques horarios prolongados (Martínez-Costa &amp; Díez Unzueta, 2005). Todas las emisoras––del tipo de cobertura (nacional, regional o local) y de la titularidad que sean (públicas, privadas o comunitarias)––han hecho del magacín el producto que define la programación, tanto de lunes a viernes como los fines de semana. Su presencia se ha hecho indispensable para estructurar los paquetes de programación y dotar de personalidad y estilo diferencial a las emisoras. En cuatro décadas, ha pasado de ocupar un lugar secundario en la programación a ser “el programa” por excelencia, producto de referencia y de <em>prime time</em> de las empresas de radiodifusión, para las que la programación se orienta “a la conquista y mantenimiento de la audiencia y la rentabilidad económica o de servicios” (Cebrián Herreros, 2004, p. 54).</p>
  <p>Sin embargo, los datos que ofrecen los estudios de audiencia comienzan a mostrar un cambio de tendencia, cuestionando un tipo de programa hasta ahora de éxito narrativo y comercial. En concreto, el desplazamiento del <em>prime time</em> de los tramos de entretenimiento de 10 a.m. al mediodía a los tramos más informativos de 7 a.m. a 9 a.m. ha llevado a algunas emisoras a fragmentar los tradicionales magacines en dos bloques bien diferenciados entre las 6 a.m. y 9 a.m. y las 9 a.m. y el mediodía, con voces, registros y marcas diferentes, cuestionando su supervivencia narrativa.</p>
  <p>Mediante el análisis de los datos de audiencia de la radio generalista en Argentina y España, este trabajo compara el comportamiento de las audiencias de los programas líderes del <em>prime time</em> para descubrir las similitudes y diferencias entre dos mercados radiofónicos de referencia en el mundo hispano. Para ello, primero se describirán las características formales y narrativas de cada magacín, para luego comparar la audiencia de cada uno de los programas.</p>
  <p>Los programas analizados son los que, según los estudios realizados por IBOPE Argentina y EGM España (Estudios General de Medios), acumularon la mayor audiencia en 2013, de lunes a viernes,  de 6 a.m. a 1 p.m. A saber: en Argentina <em>Cada mañana</em> y <em>Lanata sin filtro</em> (Radio Mitre), <em>Hola Chiche y El oro y el moro</em> (Radio 10), <em>Magdalena tempranísimo y La mañana</em> (Continental), <em>Empezando el día y Ciudad Goti K</em> (La Red); y en España <em>Hoy por hoy</em> (Cadena Ser), <em>Herrera en la onda</em> (Onda Cero), <em>La mañana</em> (Cadena Cope) y <em>Las mañanas de RNE</em> (Radio Nacional de España). </p>
  <p>Con todo ello se propone describir el momento de cambio en la forma de contar de la radio generalista en el que ha sido hasta ahora su programa estrella, el magacín, con el fin de vislumbrar tendencias que luego puedan aplicarse en otros mercados de la radio.</p>
