<section>
  <h2>Metodología</h2>
  <p id="note-line-2">El objetivo de este trabajo es describir los cambios que experimenta el magacín al hilo del comportamiento de las audiencias de los programas líderes del <em>prime time</em> para descubrir las similitudes y diferencias entre dos mercados radiofónicos de referencia en el mundo hispano, con el fin de vislumbrar tendencias que luego puedan aplicarse al estudio de otros mercados de la radio.<sup><a href="#note-2">[2]</a></sup></p>
  <p>El enfoque de este estudio se formula de la siguiente manera: los programas magacines––que han sido hasta ahora la columna vertebral de la programación generalista en los mercados de la radio argentina y española––no incrementan audiencia a pesar de los cambios de contenido introducidos y a pesar de que su audiencia envejece. </p>
  <p>Las siguientes son las preguntas de investigación: ¿Qué elementos comunes y qué aspectos diferenciales tienen los magacines analizados? ¿Qué hora del día es la más escuchada de lunes a viernes? ¿Qué contenido predomina en los tramos de programas de máxima audiencia? ¿El entretenimiento y la radio desenfadada incrementan la audiencia del radio? ¿Qué papel juega la información en los programas magacines? ¿Los presentadores siguen siendo el elemento diferenciador de los programas magacines? ¿Contribuyen los magacines a mantener el índice de audiencia de la radio generalista frente a la radio especializada o temática? ¿Los magacines consiguen rejuvenecer la audiencia de radio? </p>
  <p>Para ello y tras esta revisión teórica del concepto y las características del magacín, se presenta brevemente el contexto del mercado de la radio en Argentina y España (su estructura de emisión, la identificación de los principales grupos, la inversión publicitaria y la distribución de la audiencia), para luego describir las características formales y narrativas de cada uno de los programas objeto de análisis y confrontarlos con el comportamiento de la audiencia.</p>
  <p>Para la descripción de los programas se elaboró una ficha de análisis de contenido en la que las autoras registraron los datos del último trimestre de 2013 correspondientes a la emisora (tipo, titularidad, ranking, perfil de audiencia) y a cada uno de los programas (cuota de audiencia, horas y día de emisión, presentador, distribución de contenidos por horas de emisión) a partir de los datos proporcionados por las emisoras y confrontados con la escucha de los programas cuando fue necesario.</p>
  <p id="note-line-3">Para la presentación de los datos correspondientes al comportamiento de la audiencia se tuvieron en cuenta los informes anuales correspondientes al año 2013 de IBOPE Media Argentina y del Estudio General de Medios de AIMC (Asociación para la Investigación de los Medios de Comunicación) España, de amplio reconocimiento en el sector<sup><a href="#note-3">[3]</a></sup>. Como se ha mencionado en la introducción, los programas analizados son los que, según estos estudios, acumularon la mayor audiencia ese año, de lunes a viernes, de 6 a.m. a 1 p.m.</p>
</section>
<section>
  <h2>El mercado de la radio en Argentina y España</h2>
  <p>La radio, como parte del ecosistema de medios, integra una industria muy consolidada en Argentina y en España, tanto por su tamaño como por el grado de acceso de la población a los productos informativos y de entretenimiento.</p>

  <h3>Argentina</h3>
  <p>En Argentina, se trata de un mercado amplio, integrado por diversas empresas de medios y particulares que generan una oferta informativa abundante y variada. Según las cifras de inversión publicitaria que recoge la Asociación de Agencias Argentinas de Publicidad (AAAP), Argentina es el tercer mercado de medios en Latinoamérica, detrás de Brasil y México, con cerca de 2.000 millones de dólares de inversión anual. La radio concentra una cuota del 8% de dicha inversión. </p>
  <p>La industria de la radio se ve afectada por la alta densidad de población en la capital federal y el Gran Buenos Aires, donde hay más empresas de medios, más productos, más anunciantes y, por lo tanto, más inversión publicitaria. Si bien el mercado ofrece variedad informativa, es cierto que la audiencia y los anunciantes se concentran en pocas emisoras, la mayoría de ellas en Buenos Aires. </p>
  <p>Según las mediciones publicadas en 2014 por la empresa IBOPE, en Buenos Aires, considerando las 24 horas, de lunes a domingo, todas las edades y niveles socioeconómicos, y ambos sexos, la radio líder en audiencia es Mitre AM 790, con 40,9% de <em>share</em>. Bastante detrás se ubican Radio 10 y La Red, con 17,6% y 13,3% respectivamente. Entre las FM, las más escuchadas son Pop Radio, con un <em>share</em> de 12.5%, FM 100, con 11,2% y FM Metro, con 9%. </p>
  <p>El mercado de la radio en Argentina está volcado fundamentalmente a la actividad privada y, regido por una lógica comercial, se financia a través de la publicidad. Aunque la presencia del Estado es menos representativa que en otros países, cuenta con un Servicio Oficial de Radiodifusión (SOR), conformado por un conjunto de emisoras públicas, sostenidas por el Estado. Por lo tanto, hay un grupo mayoritario de radios comerciales y dos grupos más pequeños, uno integrado por emisoras públicas, y otro por emisoras comunitarias, entre las que hay religiosas, rurales, de frontera y universitarias. </p>

  <h3>España</h3>
  <p>Por su parte, España tiene un mercado radiofónico mixto en el que conviven desde sus orígenes la radio comercial y la radio pública, tanto en el ámbito local y regional como nacional. Las emisoras de cobertura nacional se constituyen como cadenas de emisión y aglutinan la principal oferta de radio, tanto generalista como especializada, así como la mayor audiencia. </p>
  <p>Según los datos publicados en 2014 en el EGM, la radio española tiene una audiencia acumulada de 24.194.000 oyentes, que implica una penetración del 61% de la población. Es el segundo medio más extendido después de la televisión, y sobrepasa levemente a internet, que incrementa su penetración hasta el 60,7% en 2013. El ranking de audiencia de las emisoras generalistas con cobertura nacional está liderado por la Cadena SER con un 35,4% de <em>share</em>, seguida de Onda Cero (20,4%), Cope (12,3%) y Radio Nacional de España (8,3%). Las emisoras especializadas más escuchadas son las musicales Cadena 40 con un 16,5% de <em>share</em> de audiencia, Dial (13,1%), Cadena 100 (10,7%) y Europa FM (10,1%). La franja de concentración de mayor escucha se extiende entre las 8 a.m. y las 11 a.m., donde se acumula algo más del 30% de la audiencia diaria.</p>
