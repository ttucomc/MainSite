  <h2>Marco teórico</h2>
  <p>En la era de la abundancia de medios resulta sorprendente, como señala Fleming (2010), que la radio––el medio de radiodifusión más antiguo––siga aglutinando audiencias superiores al 90% de la población de muchos países. Fernández (2012), por su parte, señala que la radio “es el único de los medios considerados <em>viejos</em> que crece sin modificar demasiado todavía sus formatos” y que “la convergencia no genera crisis en los medios de sonido sino que por el contrario convive con su antigua expansión” (p. 15).  La aparición de internet obligó nuevamente a la radio a repensarse, como cuando buscó la diferenciación por la llegada de la televisión, y en lugar de competir con ella, se concentró en ser el medio que les permitía a las audiencias compartir la escucha con otras actividades (MacFarland, 1997).  Aprovechando su ubicuidad, hoy la radio saca ventaja de los nuevos soportes y plataformas que multiplican una vez más las posibilidades de la comunicación sonora.</p>
  <p>Buena parte de la aceptación del medio a través del tiempo se debe al programa magacín, que ha sido la estrella de la programación en la radio generalista de los últimos cuarenta años, tanto en el mercado de la radio española como en el de la latinoamericana (López Vigil, 1997; Cuní, 1999; Martí, 2000). Popularizados con el nombre de <em>talk shows</em>, también son uno de los ejes de la programación radiofónica norteamericana y europea (Turow, 1974; Armstrong &amp; Rubin, 1989; Rodda, 1995; Hutchby, 1996; Smith,1998; Dauncey &amp; Hare, 1999). A lo largo de estas décadas, este tipo de programa se ha erigido en la columna vertebral de la oferta radiofónica, concentrando audiencias masivas, prolongadas franjas horarias e intensos esfuerzos de producción.</p>
  <p>El magacín nace como un tipo de programa que persigue la captura de grandes audiencias y se transforma rápidamente en el <em>motor</em> de su oferta de contenidos, por esto algunos autores le denominan programa-tractor:</p>
  <blockquote>
    <p>El magacín es un programa de larga duración, emplazado en las franjas horarias importantes para la captación masiva de audiencia, dirigido por un/a conductor/a “estrella”, con contenidos de actualidad, dotado de importantes equipos profesionales para la redacción, locución y producción, contando con una amplia presencia de colaboradores y expertos que enriquecen los contenidos del programa (Martínez-Costa &amp; Díez Unzueta, 2005, p. 186).</p>
  </blockquote>
  <p>El magacín ofrece una estructura que combina información, opinión, entretenimiento y espectáculo, con un cambio de ritmo constante que le permite adaptarse a la audiencia.  Se trata de un tipo de programa que responde a las necesidades de la radio de hoy: <em>cambios de ritmo</em> para atender los comportamientos sociológicos de la audiencia y adaptar los contenidos a sus necesidades y situaciones laborales y de ocio; <em>un conductor con fuerte personalidad</em> que facilite la identificación y fidelice a la audiencia; y la <em>combinación de componentes fijos</em> (conductor, columnistas, secciones, tratamiento musical) <em>y variables</em> (entrevistados, agenda de temas y contenidos) para aportar al mismo tiempo continuidad y novedad. El magacín ofrece ritmo y variedad a través de tiempos cortos y heterogeneidad de contenidos y voces.  Su dinamismo y su variedad de contenidos y registros lo transforman en el tipo de programa más adecuado a la radio de nuestros días y, en consecuencia, en el de mayor éxito.</p>
  <p>Magacín es un término que caracteriza a las revistas de contenidos variados que buscan el entretenimiento del lector (Martínez-Costa &amp; Díez Unzueta, 2005). Se desplaza de la prensa escrita a la radio hacia la década 1950, primero en Estados Unidos, donde nace con un sentido más informativo y no tan asociado al entretenimiento como se muestra actualmente. El magacín es, a su vez, protagonista del paso de una radio de noticias a una radio de comunicación, que busca mayor contextualización, análisis y comentario (Cebrián Herreros, 1994).</p>
  <p>Es también un programa de variedades, se sostiene en la heterogeneidad de contenidos y es justamente en la variedad donde encuentra su verdadero atractivo, tanto para la audiencia como para los anunciantes.  Algunos autores lo consideran un “programa de programas”, en tanto incluye unidades pequeñas (secciones) en una unidad superior (programa).  Por lo tanto, como asegura Cebrián Herreros, “su mayor desafío es ofrecer bajo el manto de la unidad la mayor variedad posible de temas, voces y situaciones” (1994, p. 481).</p>
  <p>La radio encontró en el magacín la mejor manera de conseguir audiencias masivas y estables, más atractivas para los anunciantes, en parte porque se ha construido a partir de una <em>radio de estrellas</em> (Legorburu, 2004).  El conductor del magacín, bastonero que enlaza heterogeneidad de voces y contenidos, es la voz determinante en la definición de la identidad y el estilo del programa, en consecuencia, puede decirse que es el artífice del éxito o del fracaso de un programa.  La mayoría de las veces se eligen figuras o personajes conocidos (a veces por su labor en la prensa o en la televisión) con el objetivo de facilitar la identificación con el oyente.  El conductor da una impronta personal y única al programa, que se mimetiza con su estilo y provoca una fuerte dependencia en su figura.  El oyente elige escucharlo a él y, por lo tanto, la mayoría de las veces la emisora resulta un marco indispensable pero no único para conseguir el éxito de un programa. La conversación casual y la interacción moderada––o “semi-institucional”, al decir de Ilie––se instalan como uno de los rasgos distintivos del discurso de los presentadores del magacín (2001, p. 209). Por esto, algunos autores señalan que el magacín ayudó a consolidar la <em>radio de estrellas</em> frente a la radio de emisora o de contenido (Cebrián Herreros, 1994). Sin embargo, más recientemente otros autores, como Starkey (2015), identifican y señalan las limitaciones de la interacción y devuelven al presentador el papel de mero moderador.</p>
  <p>Los colaboradores también son piezas fundamentales en el entramado narrativo del magacín. Los columnistas aportan sus conocimientos e interpretaciones sobre temáticas específicas en las que son expertos. Muchas veces, a fuerza de un estilo definido y atractivo, se convierten en personajes centrales del programa, casi tan importantes como el conductor. Cronistas, locutores, productores, operadores técnicos y editores son parte de un equipo humano indispensable para completar la propuesta de cualquier magacín. Entre ellos, destacan los humoristas, artífices de la lectura cómica e irónica de la actualidad, que cosechan cada vez mayor protagonismo a través de sus intervenciones, imitaciones y relatos. </p>
  <p>La audiencia es determinante para la existencia de cualquier programa radiofónico, más aún si cabe en el magacín, en donde el oyente es un integrante más, a quien se interpela de las formas más variadas para conseguir su participación. El oyente ayuda a construir el relato y condimenta el mensaje de los profesionales con sus opiniones y aportaciones. No obstante, autores como Starkey (2004, p. 3) y Pinseler (2015, p. 69) son algo más críticos con respecto al uso de la participación “altamente gestionada” o deliberadamente provocada y filtrada de la audiencia, y llegan a calificarla como “audiencias estimadas” o “voces domesticadas”, respectivamente.</p>
  <p>El magacín distribuye el tiempo total en unidades narrativas menores, con autonomía pero dependientes del conjunto: las secciones. Se considera sección a todo segmento del programa con una determinada periodicidad (diaria, semanal, etc.), con un horario establecido, enmarcado por piezas artísticas, música o por la referencia del conductor, identificable para el oyente, que puede incluir o no un género específico (noticia, crónica, comentario, etc.).</p>
  <p>Para construir la personalidad y la unidad de los segmentos que se integran para crear el magacín, se hace uso de la música y de las piezas artísticas. La música se utiliza como contenido a través de temas musicales, y como elemento de realización, es decir, como recurso para crear separadores formales.  La artística de un magacín comprende todas las piezas que combinan música y palabra para identificar el programa y sus secciones, enlazar contenidos, establecer transiciones y narrar historias. El magacín encuentra en la música y en la artística dos recursos que ayudan a entretejer las partes para conseguir variedad, unidad y coherencia en el todo. </p>
  <p>Quienes forman parte de un programa son, de un modo u otro, responsables de su contenido. Sin embargo, los magacines cuentan con un equipo de producción encargado de pensar los contenidos de modo integral, considerando todas las variables: el estilo y los intereses del presentador, los colaboradores con los que cuenta, los recursos técnicos, las peculiaridades de la emisora. La producción trabaja tanto en la idea general del programa como en las decisiones inmediatas que exige un programa en directo. Busca integrarse en el todo que supone la programación en su conjunto y, al mismo tiempo, diferenciarse, generando una propuesta con identidad propia. </p>
  <p>Aunque estas características esenciales de todo programa magacín se mantienen en los últimos años, y son más o menos homogéneas tanto en la radio argentina como la española, la fórmula de éxito se ha ido adaptando tímidamente a las nuevas formas y necesidades de la escucha. Más ligado a la información de actualidad en un principio, el entretenimiento ha ganado terreno en los magacines de la radio. Los tratamientos narrativos de la información del día, producidos a partir del ritmo de la actualidad, se mezclan ahora con el relato en continuidad y con una producción más creativa y cuidada. El presentador sigue siendo el pilar sobre el que se construye el programa, pero ganan terreno las voces de los expertos y, con ellos, los géneros de autor y de opinión. El magacín ha sido el programa estrella de la radio generalista, y ahora comienza a formar parte de la programación de los tramos de mañana de la radio especializada musical, que busca entretener y ganar audiencia con los denominados <em>morning shows</em>. Los magacines nacen como apuesta de la radio comercial y, con el tiempo, la radio pública también ha replicado este tipo de programas. </p>
  <p>Estos cambios percibidos en la ideación y puesta en antena del magacín radiofónico hacen interesante el estudio de sus audiencias, puesto que su duración, estructura y contenidos han ido evolucionando ligado a los comportamientos de las audiencias.</p>
