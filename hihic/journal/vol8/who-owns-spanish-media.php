<section>
  <h2>Abstract</h2>
  <p>This paper examines the current state of ownership of Spanish-language media in the United States by focusing on several media sectors: television stations, television networks, cable networks, radio stations, newspapers, and magazines. Across media sectors, the analysis reveals that most Spanish-language individual properties are not owned by Latinos/Hispanics, but by larger corporations that reside in the general market. Factors behind this pattern of ownership are discussed, along with recent policy decisions to try to improve the percentage of minority ownership across the United States, especially for Hispanics/Latinos.</p>
  <p>
    <strong>Keywords:</strong> Hispanics, Latinos, Spanish-language, media, ownership, television, radio, newspapers, cable, magazines
  </p>
</section>
<hr />
<section>
  <h2>Introduction</h2>
  <p>The number of Hispanics living in the United States is estimated to be somewhere around 54 million people, or approximately 17.1% of the total population, according to the latest government figures (State and Country Quick Facts, 2014). This number does not take into account the undocumented Hispanic population that may have been living in the United States for years (Passel, 2005). That figure, at best an educated estimate, is somewhere around 8-12 million people.  In terms of raw numbers, the Hispanic population is the fastest growing ethnic group in the United States.</p>
  <p>The rise of the Hispanic population, along with the growing economic clout of Hispanic households, long ago caught the interests of major corporations wanting to invest in this expansive market.  This was especially true for the broadcast media, their marketers, and advertisers, all desiring to expand their market share and generate revenues from a growing ethnic segment. Owners across the non-Hispanic “general market” began to acquire Spanish-language companies and to change/convert formats to Spanish to reach Hispanic eyes and ears. The growth of Spanish-language broadcasting in the United States has been quite remarkable (e.g., Castañeda Paredes, 2003; Wilkinson, 2009; 2016).</p>
  <p>Yet, despite the rapid growth of the Hispanic population and the rise in the number of Spanish-language TV networks, broadcast stations, and print publications, how much of what we know as Spanish-language media is actually owned by Hispanics? This paper takes a contemporary look at the subject of Spanish-language media ownership in the United States by focusing on the major media sectors. In order to provide a proper context for this study, we first examine the concept of media ownership in the United States, and the key concepts that drive ownership policy.</p>
</section>
<section>
  <h2>Background</h2>
  <p>Media ownership is an area of study that encompasses several theories and concepts (Harcourt &amp; Picard, 2009). Ownership of the media in the United States (and many developed nations) was initially driven by the theory of <em>scarcity</em>—early on there were few channels available to license (e.g., the case of broadcasting) and therefore licenses were awarded to those owners who would best operate in the public interest (Hazlett, 1990). The scarcity paradigm dominated the regulatory view of media ownership in the U.S. until the 1980s, when a <em>marketplace</em> view of regulation emerged (Fowler &amp; Brenner, 1982), recognizing how technology had created numerous channels (largely through the development of cable television) and eliminating concerns related to scarcity.  </p>
  <p>Marketplace theory influenced policymakers to liberalize broadcast ownership limits in 1984.  This marketplace view continued to expand with the introduction of the Internet and the World Wide Web in the 1990s, leading to the Telecommunications Act of 1996, which further relaxed ownership restrictions and allowed for cross-media ownership (networks owning more than one network; publishers owning TV stations, etc.).  One outcome of marketplace theory was a concern over the growing concentration of media ownership, the idea that large media conglomerates only offered homogeneous products, limited diversity of opinion, and strongly influenced public opinion (Doyle, 2002).  </p>
  <p>One critic of media consolidation was Bagdikian (2004), who argued that control of most of the news media in the United States was vested in just seven companies, down from nearly 50 in 1983. Consolidation can occur via horizontal and vertical integration, two other concepts key to any discussion of media ownership (Albarran, 2009). In horizontal integration, a media company may hold interests in creating content for television and film; publishing in various forms; and digital media via websites, applications, and social media. Vertical integration occurs when a company has the ability to control the various aspects of production, distribution and exhibition of media content. Horizontal integration or cross-media ownership is what has led many legacy companies to enter Spanish-language media. For example, NBC had a long history in network television before acquiring Telemundo in 2002.</p>
  <p>Conversely, the theory of <em>pluralism</em> runs counter to the notion of concentrated media. The pluralist theory of media ownership suggests that owners are for the most part responsible in the way that they manage information, because media content is ultimately shaped by consumer demand, and thus provide content that the public wants and desires (Bennett, Curran, &amp; Wollacott, 1982). Pluralists argue that large media conglomerates are complex systems, and that owners lack the ability to interfere in the day-to-day operations and influence the creation of content.</p>
  <p>Mobile media and social media debuted during the 2000s, creating new “platforms” that led to widespread fragmentation of the audience. In particular, mobile/social media made possible all types of audience-created content that could be shared easily and quickly. Streaming media content has nearly eliminated the watching of live television in many households, with a growing number of companies offering original content to audiences for a subscription fee (Netflix, Amazon Prime, and Hulu being the most prominent). These technological advances began to raise many questions for policymakers over how to address issues like security, privacy, and ethics regarding content. </p>
  <p>This brief overview of media ownership illustrates the challenges policymakers face in determining ownership requirements.  One aspect not yet discussed—but germane to this study—concerns itself with the actual owner, and the role of minority ownership of the media in the United States. The ethnic makeup of the United States is diverse, represented by Anglos, African Americans, Hispanics, Asian Americans, Native Americans, and other ethnic groups. It is generally expected—although empirically challenging to assess—that minority ownership of the media would tend to be better oriented and more sensitive towards the needs of the ethnic audiences they serve. Regretfully, minority ownership of the media in the United States has a bleak history despite several policy initiatives to spur greater minority ownership (Albarran &amp; Fender, 2006; Honig, 1984).  </p>
  <p id="note-line-1">In the case of broadcasting, the Federal Communications Commission<sup><a href="#note-1">[1]</a></sup> conducts a quadrennial review of broadcast ownership as required by the Telecommunications Act of 1996 (Federal Communications Commission, 2014). Based on the latest information in the report, the current state of Hispanic-owned broadcast media is woeful. Quoting directly from the commission’s report (p. 3):</p>
  <blockquote>
    <p><em>Broadcast ownership and ethnicity</em>.  Hispanic/Latino persons collectively or individually held a majority of the voting interests in 571 broadcast stations, comprised of 42 full-power commercial television stations (3.0%) of 1,386 stations; 155 low-power television stations, including Class A stations (9.4%) of 1,651 stations; 194 commercial AM radio stations (5.2%) of 3,737 stations; and 180 commercial FM radio stations (3.2%) of 5,714 stations.  </p>
  </blockquote>
  <p>There are numerous reasons for these dismal numbers. One is the long-term failure of regulatory policies in the United States to provide for greater minority ownership. Another widely cited reason is the lack of capital for minority owners, and the inability to raise funding for acquisitions among lenders. But what is striking is the disparity between the nation’s largest ethnic group—Hispanics at approximately 17% of the population—and media ownership that, in the highest category (low-power TV stations), only reaches 9.4% based on the Federal Communications Commission (FCC) 2014 report.  Further, is it a surprise that low-power television stations are typically the poorest stations in terms of generating advertising revenues in the United States?</p>
  <p>The Spanish-language newspaper sector in the U.S. also illustrates the disparity of small versus large owners and minority ownership. Alphabetically, the first Spanish-language newspaper, <em>Ahora</em> of New Jersey, demonstrates the concern that Hispanic media has for the population it serves. <em>Ahora</em>’s website notes that the owner, the nonprofit Puerto Rican Congress of New Jersey, helped found the paper in 1970 because the Spanish-speaking members of the community needed information in a language they could understand. The paper’s mission is “to report, guide, educate, and entertain the Hispanic community” (<em>Ahora</em> News, 2014, para. 2). </p>
  <p>According to Eddie García, the publisher of the first Spanish-language publication in Southwest Arkansas, the privately held weekly <em>Amigo</em>, the newspaper still maintains its core mission: “to support the Hispanic community by providing information about religion, education, culture, tradition, family, and community” (personal communication, March 13, 2016). Contrast these two papers to <em>Al Día</em>, in Dallas-Fort Worth, the number five media market (<em>Al Día</em>, 2014). <em>Al Día</em> serves the Hispanic population in the DFW market, but is owned by the A. H. Belo Corporation, a publicly held company that has no minority ownership representation. </p>
  <p>Given this introduction, our goal in this study is to explore the following research questions related to ownership of Spanish-language media properties:</p>
  <p class="rq-list"><strong>RQ1.</strong> Who are the top companies targeting ethnic audiences in the United States?</p>
  <p class="rq-list"><strong>RQ2.</strong> Across individual media segments, how many of the top companies are Hispanic-controlled?</p>
  <p class="rq-list"><strong>RQ3.</strong> Based on the findings of these research questions, what can be said about the broader question of “Who owns Spanish media in the United States”?</p>
</section>
<section>
  <h2>Methodology and Analysis</h2>
  <p>This study utilized a combination of historical, financial, and legal analysis to address its three research questions. Finding financial data on media companies can be challenging due to differences in corporate accounting methods, which can vary considerably; whether or not the company is public or privately held; and any discrepancies reported on financial statements that can affect the interpretation of the data. Thus, while this may appear to be a descriptive study, the actual data analysis was quite demanding and required considerable investigation and assessment.</p>
  <p>Specific sources included corporate annual reports, financial websites and other Internet sources, research from non-profit entities, and government documents, all of which are detailed in the reference list and/or the tables presented below.  In the case of annual reports, we used the most recent reports available, with the understanding that corporations are allowed to establish their own fiscal years, which resulted in some company information based on 2014 rather than 2015.  </p>
  <p>To present a broad overview of Spanish-language media ownership, we limited our focus to the top five companies across different segments of the media as ranked by either revenue or other variables, recognizing that the top five would represent the dominant companies in each sector. Our interest was to see how many of these companies in each sector were owned by Hispanics.</p>
</section>
