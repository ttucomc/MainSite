<section>
  <h2>Discussion</h2>

  <p>Several observations can be drawn from examining the third research question, “Who owns Spanish media in the United States?” First, based on the segment analysis, it is clear that the majority of the top-performing firms offering Spanish-language content are, in fact, owned by non-Hispanics. In many cases, the owners are large corporations primarily engaged in the general media market that over time began to transition into the Spanish-language market. There are a few exceptions to this trend, with companies like SBS, Liberman, and ImpreMedia reflecting Hispanic ownership. But these are the exception, not the rule. Further, these are small companies compared to giants like Comcast and Univision, which dominate the broadcast and cable segments with their holdings.</p>
  <p>This analysis also supports the observation that potential minority owners have likely experienced long-term challenges to acquire the investment capital to purchase available media properties and finance new startups (Christiano, Eichenbaum, &amp; Trabandt, 2014; Coleman, 2004). This disparity in Hispanic ownership can, in part, be attributed to a lack of available capital, as well as to stringent capital requirements for lending. We note this disparity even as the Hispanic population in the United States continues to grow. The difficulty in obtaining capital is a barrier to overcome in the private sector, in part heightened by increasing regulatory burdens placed on financial institutions as part of reforming the banking sector following the 2008 great recession (Cetorelli &amp; Goldberg, 2012).</p>
  <p>A third observation regarding the lack of minority ownership lies in the failure of regulators and the government to provide a meaningful set of policies to promote minority ownership across all ethnic groups. There have been numerous policy efforts to try to improve ownership in the broadcast sector (Albarran &amp; Fender, 2006; Honig, 1984), yet the overall results have demonstrated only minimal progress. This is a long-term policy failure by regulators that anyone examining minority ownership of the media quickly recognizes. Ownership simply does not reflect the representation of the ethnic population in the United States. </p>
  <p>The FCC is taking another small step towards trying to improve minority ownership. On March 30, 2014, the FCC announced that, moving forward, television joint sales agreements (JSAs), which allow a broadcaster to sell advertising time for another broadcaster in its geographic area, must come into compliance with the same rules governing radio JSAs.</p>
  <p>In the radio market, if one broadcaster sells more than 15% of another station’s time, it is deemed to have “attributable ownership interest” for purposes of the FCC’s media ownership limit. This attribution rule now applies to television broadcasters across the country. The National Hispanic Media Coalition hailed the decision as “a step towards ensuring fair media ownership rules that increase competition, as it will push some broadcast conglomerates over media ownership limits, requiring that they abandon or minimize the use of JSAs” (NHMC statement, 2014).</p>
  <p>Optimistically, we can hope that this latest action may help open the door for additional Hispanic owners in the broadcast television market. Will it be enough to increase the percentage of Hispanic owners for media properties across the United States? Sadly, our guess is that it will not create much in the way of change. It will take several years of observation to determine the success or failure of this latest effort to improve minority ownership.</p>
</section>
<section>
  <h2>Conclusion</h2>

  <p>The study of media ownership, especially for minority media, is an important area of research that needs to be continued by media scholars on an annual basis. Media ownership is complicated, and there are many ways to assess ownership regulation. Harcourt and Picard (2009, p. 15) claim ownership regulation is a tool “employed to ensure optimal outcomes in terms of social, cultural, political, and economic effects with regards to media structure and performance.” In a country as large and diverse as the United States, these are formidable goals to attempt to reach, given so many conflicting views and approaches, along with a federal government that has proven time and again to be dysfunctional and gridlocked. Yet, this study does indicate a willingness on the part of Hispanic owners, and their business operations, to provide social and cultural direction to their audiences. Further, there appears to be a desire to provide relevant political news on both the national and local levels.</p>
  <p>Despite the small number of true Hispanic owners across the media sectors, Spanish-language media continues to grow and prosper across all sectors except print. One can only wonder how greater Hispanic ownership would affect the long-term viability of the overall industry.</p>
  <p>While this analysis hopes to clarify the question of who really owns Spanish-language media in the United States, additional work needs to be done on this complicated and frustrating topic.  Future research is warranted to address any changes in ownership patterns across the media sectors. This study was limited by its focus on traditional media segments, and limiting the analysis in each sector to just the top five entities.  Future studies could address the ownership patterns found in the new/emerging media environment involving mobile and social media, and the extent to which these areas feature any minority ownership.</p>
