<section>
  <h2>Other Hispanic Media Companies</h2>
  <p>EC Hispanic Media is a private company founded by Martha de la Torre and Joe Badame with the purpose of “connect[ing] sellers of various products and services within the Hispanic community, while also providing Latinos with educational resources and how-to lifestyle improvement” (EC Hispanic Media, 2014a, para.1). Its <em>El Clasificado</em> began as a direct mail publication and moved to a bulk drop in high-traffic areas, increasing the circulation from 10,000 copies to over a half-million (EC Hispanic Media, 2014a). Founder de la Torre claims that EC Hispanic Media is “the most widely distributed free weekly Spanish-language classified publication in the United States” (EC Hispanic Media, 2014b, para. 1). The company has also been active establishing digital platforms (EC Hispanic Media, 2014a). </p>
  <p>Entravision Communications is a public company trading on the NYSE (Entravision, 2014). Approximately 12% of the stock is owned by Walter Ulloa, the chairman and chief executive officer; another 10% is owned by Univision Communications (Entravision Ownership Report, 2015). The company reported 2015 revenues of $254 million (Entravision 2014 Annual Report, 2016). The company’s website notes it is a “Spanish-language media company” owning or operating 58 television stations that form “the largest affiliate group of both the top-ranked Univision network and Univision’s UniMás network” (Entravision, 2014).  In addition, Entravision either owns or operates 49 radio stations with a strong digital presence. It primarily operates in Texas, Florida, and several Southwestern states (Entravision, 2014).  </p>
  <p>ImpreMedia was a privately held company controlled by founders John Paton (currently chief executive officer of Digital First Media) and Ignacio E. Lozano Jr., and later by Lozano’s son José I. Lozano and finally by his granddaughter, Monica Cecilia Lozano, the current CEO (ImpreMedia, 2014). In 2012 the company became a subsidiary of Argentina-based La Nación S.A. (Argentina’s La Nación takes over, 2012). The company owns newspapers located in Los Angeles, New York, Chicago, San Francisco, Orlando, and Houston (ImpreMedia, 2014). ImpreMedia’s most important newspaper is <em>La Opinión</em>, based in Los Angeles. According to its website, <em>La Opinión</em> is “the leading” Spanish-language newspaper in America” (ImpreMedia, 2014, para. 1).   </p>
  <p>Liberman Broadcasting Inc. is privately held, with founder José Liberman (CEO) and son Lenard D. Liberman (president) as the principal owners. The company is located in Burbank, California. Liberman calls itself the “largest privately held minority-owned Spanish-language broadcaster in the United States” (Liberman Broadcasting, 2014, para. 1). It owns nine television stations in Illinois, Texas, Colorado, California, Arizona, New York, and Utah, as well as the Estrella network. It also owns 16 radio stations in Texas and California (Liberman Broadcasting, 2014). </p>
  <p>Spanish Broadcasting System’s website claims SBS “is the largest publicly traded Hispanic-controlled media and entertainment company in the United States” (Spanish Broadcasting System, 2014a). The company’s stock is traded on the NASDAQ and in 2014 had gross revenues of $146.28 million (Spanish Broadcasting System, 2014b). The company owns 20 radio stations in New York, Los Angeles, Miami, Chicago, San Francisco, and San Juan, Puerto Rico. The television stations are located in Miami, Las Vegas, and Guaynabo, Puerto Rico. The company also produces concerts and operates LaMusica.com (Spanish Broadcasting System, 2014c). </p>
  <p>SBS is controlled by the Alarcón family. Raúl Alarcón Sr. founded the company and encouraged Hispanic employment, a policy that continued when the company went public and his son assumed leadership (Spanish Broadcasting System, 2014a). Alarcón Sr. was also a force in the music industry receiving a posthumous induction into <em>Billboard</em>’s Latin Music Hall of Fame (Santana, Daddy Yankee to be honored, 2009).</p>
  <p>Tribune Publishing Company became publicly traded in August 2014 when it separated from the Tribune Company; it is not minority-controlled. The company operates several newspapers throughout the United States, as well as three commuter tabloids, three magazines, and the Tribune Content Agency. Titles in the newspaper group include the <em>Chicago Tribune</em>, the <em>Los Angeles Times</em>, the <em>Orlando Sentinel</em>, the <em>Sun-Sentinel</em>, and the <em>Baltimore Sun</em>. In terms of the Hispanic market, the Tribune Publishing Company operates <em>El Sentinel del Sur de la Florida</em> in Fort Lauderdale, Florida, and <em>El Sentinel</em> in Orlando, Florida, both managed through the larger English-language papers in their respective communities. The company also publishes Spanish-language commuter tabloids—<em>Hoy</em> in Chicago and Los Angeles, as well as <em>El Sentinel</em> in Miami (Tribune Media Company, 2014). </p>
  <p>To summarize, this review of the nine leading companies operating in the Spanish-language marketplace found that of the five publicly traded companies, only Spanish Broadcasting Systems is Hispanic-controlled. Of the three privately held companies, Liberman and EC Media are Hispanic-controlled, while La Nación S.A. is foreign-controlled.  Given these broad findings, we now turn our attention to the ownership across individual media markets.</p>
