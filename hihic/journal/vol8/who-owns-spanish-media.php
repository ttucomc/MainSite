  <p>In terms of the three companies targeting Hispanics, Comcast is a publicly held corporation, trading on the NASDAQ and headquartered in Philadelphia. According to its 2015 annual report, the corporation produced revenues exceeding $65.6 billion with total assets of over $158.8 billion (Comcast, 2016). Comcast holdings include the second-largest Spanish-language broadcast television network, Telemundo, the cable channel NBC Universo, and 15 local Telemundo TV stations.</p>
  <p>Telemundo programming includes <em>telenovelas</em>, sports, reality programs, news, and feature films. In addition, Telemundo maintains a robust Internet and mobile presence through its Telemundo Digital Media division. Telemundo International also distributes programs worldwide and ranks as the second-largest international provider of Spanish-language content behind Univision.</p>
  <p>Univision began as a private company, known as the Spanish International Network, in 1962. In 1996 it traded on the New York Stock Exchange as Univision, and in 2007 it became a subsidiary of Broadcast Media Partners, Inc., a group of private equity firms that have no Hispanic ownership (Univision, 2014). Univision Corporation’s revenue in 2014 was $2.9 billion, up from $2.6 billion in 2013 (Marvasti, 2015).  The company operates the Univision and UniMás networks, a number of cable/digital networks, the largest group of Spanish-language radio stations in the United States, mobile apps, and digital properties.  The company has production facilities in Miami, New York, and Los Angeles (Univision, 2014).  Univision delayed plans to conduct an initial public offering in December 2015, but expected to do so in the near future (Marvasti, 2015).</p>
  <p>McClatchy is a publicly held company trading on the New York Stock Exchange. The company reported net income of $1.30 billion in 2015, including an increased reliance on digital revenue (McClatchy Company, 2015). Its corporate website notes: “The McClatchy Company is a leading news and information provider dedicated to the values of quality journalism, free-expression and community service” (McClatchy Company, 2013, para. 1). The company has a proud history dating from California’s Gold Rush when James McClatchy began publishing the <em>Daily Bee</em> (now <em>The Sacramento Bee</em>). In more recent times, it followed a plan of expansion in both the traditional and digital publishing areas, and information gathering in the national and international arenas (McClatchy Company, 2013). </p>
  <p>The most significant Spanish-language newspaper in the McClatchy chain is <em>El Nuevo Herald</em>, serving the nation’s third-largest Hispanic market, located in Miami and southeast Florida (<em>El Nuevo Herald</em>, 2014).  The daily circulation for <em>El Nuevo Herald</em> is approximately 54,000, making it the second-largest Spanish-language daily in the nation, and almost 69,000 on Sundays, the largest circulation for a Sunday paper (El Nuevo Herald, 2014). </p>
  <p>In addition to the five largest corporations operating ethnic media segments, there are many other smaller organizations operating across the Spanish-language media space. These companies are detailed below, listed in alphabetical order.</p>
